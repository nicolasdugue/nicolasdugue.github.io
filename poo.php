					<h2 class="title">Le chiffre de Hill</h2>
					<div style="clear: both;">&nbsp;</div>
					<div class="entry">
					<p>
						Le chiffre de Hill est une méthode de cryptage symétrique qui utilise certaines propriétés du calcul matriciel. Au fil de ce TD, nous proposons de mettre en place cette technique de cryptage/décryptage de messages textuels. Ce TD permet ainsi de se réapproprier Java tout en introduisant quelques concepts objet à travers un projet de cryptographie intéressant.
