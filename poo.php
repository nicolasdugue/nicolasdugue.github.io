						A travers l'apprentissage avancé
du langage Java, ce module introduit les bonnes pratiques de programmation objet. L'encapsulation, l'héritage et la conception d’applications en équipe sont au coeur de ce module. Mais la gestion des exceptions, la maitrise des flux et la bonne utilisation des collections font aussi partie des notions clés du module. Cet apprentissage est une base
nécessaire à tous les modules de programmation avancée en Java tels que ceux dédiés à la conception d’architectures webs sophistiquées (j'enseigne également les Design Pattern en Java en M1 MIAGE et la programmation Web et Swing en Java en M1). Les sujets qui suivent sont fournis au format PDF et LaTeX et parfois avec des extraits de code. Ils représentent une partie des sujets que j'ai élaborés pour compléter et renouveller le module.

