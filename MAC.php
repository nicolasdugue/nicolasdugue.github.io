					<div class="entry">
					<p>
						Ce module se base sur les concepts
enseignés en Programmation Orientée Objet en deuxième année de licence. Il les développe de façon poussée en présentant les bonnes
pratiques en termes de "patrons de conceptions" (design patterns). Ainsi, l’accent est mis sur l’évolutivité et
la modularité du code, la conception d’application en équipe ainsi que les méthodes AGILE. Ici, je mets à disposition un sujet utilisé en fin d'année. Ce sujet propose le refactoring d'une application multi-agents simulant la propagation d'une infection de type Biohazard (Zombies !). Une version de l'application, fonctionnelle, mais mal codée et peu évolutive est fournie aux étudiants. Après l'analyse de cette application, il leur est demandé d'en modifier l'architecture et d'y ajouter des évolutions, comme notamment la création d'un Logger dans le style de Log4j ou la gestion de sessions d'expérimentations. Les patrons de conception décorateur, stratégie, commande et fabrique sont appliqués ici.<br />
La visualisation de la propagation est programmée avec la librairie Graphstream développée à l'Université du Havre.<br />
